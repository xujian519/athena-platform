def get_expanded_intent_examples() -> dict:
    """获取扩充意图示例库"""
    return EXPANDED_INTENT_EXAMPLES


def count_examples() -> dict:
    """统计各意图类型的示例数量"""
    counts = {}
    total = 0
    for intent, examples in EXPANDED_INTENT_EXAMPLES.items():
        count = len(examples)
        counts[intent] = count
        total += count
    counts["_total"] = total
    return counts


