#!/usr/bin/env python3
"""
手动创建备忘录指南
"""

import subprocess
import time

def show_manual_guide():
    """显示手动创建备忘录的指南"""
    print("\n📝 手动创建《钟之歌》备忘录指南")
    print("=" * 60)

    print("\n步骤1：备忘录应用已经打开，请按以下操作：")
    print("-" * 40)
    print("1. 在备忘录中点击 '新建备忘录' 或按 Command + N")
    print("2. 输入标题：《钟之歌》")
    print("3. 复制以下内容到备忘录中：\n")

    # 提供可复制的内容
    content = """作者：徐健
创作日期：2023年11月9日

钟啊，张开你银铃般的笑嘴，
请向我吐露真情原委：
"你终年蛰居陋室，
孤零零，只有狐鼠作陪。
告诉我：
你洪亮的嗓音谁造就？
你动听的歌声又是谁师授？"

"阁楼阴冷昏暗，
身处高塔顶尖。
我望穿风雨云层，
目睹人世间痛苦、忧愁。
我以智慧造化了美，
如此歌唱，如此鸣奏，
你会感到意外？"

赏析：
这首诗通过钟的视角，展现了在孤独中坚持创造的美好。
钟虽身处高塔，却能看到人间疾苦，并从中提炼出美的力量。

保存时间：2025年12月15日
保存人：小娜"""

    print(content)
    print("-" * 40)

    print("\n步骤2：完成保存")
    print("1. 内容输入完成后，备忘录会自动保存")
    print("2. 您可以拖动备忘录到'诗歌'文件夹中（如果有的话）")

    print("\n💡 备用方案：")
    print("- 桌面上已创建'钟之歌.md'文件")
    print("- 您可以将这个文件拖入备忘录中")
    print("- 或使用Markdown编辑器查看")

def open_notes_app():
    """打开备忘录应用"""
    print("\n📱 正在打开备忘录应用...")
    subprocess.run(['open', '-a', 'Notes'])
    time.sleep(2)
    print("✅ 备忘录应用已打开")

if __name__ == "__main__":
    open_notes_app()
    show_manual_guide()