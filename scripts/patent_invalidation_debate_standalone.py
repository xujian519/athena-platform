    def generate_response(self, opponent_argument: str, round_num: int) -> str:
        """生成辩论回应"""

        # 选择未使用的论点
        available = [a for a in self.arguments if a not in self.used_arguments]

        if not available:
            # 如果所有论点都用过了，重新开始
            self.used_arguments = []
            available = self.arguments

        # 选择下一个论点
        selected = available[0]
        self.used_arguments.append(selected)

        return selected


