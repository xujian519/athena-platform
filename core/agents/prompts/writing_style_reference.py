# ============================================================================
# 七、写作风格管理器类
# ============================================================================

class XujianWritingStyleManager:
    """徐健写作风格管理器"""

    def __init__(self):
        self.version = "1.0"
        self.last_updated = "2026-02-03"

    def get_style_guide(self, agent_name: str) -> str:
        """根据智能体名称获取写作风格指南"""
        agent_style_map = {
            "xiaonuo": STYLE_A_PERSONAL_NARRATIVE,
            "xiaona": f"{STYLE_B_PROFESSIONAL_ANALYSIS}\n\n{STYLE_A_PERSONAL_NARRATIVE}",
            "xiaochen": STYLE_A_PERSONAL_NARRATIVE,
            "athena": STYLE_B_PROFESSIONAL_ANALYSIS,
        }
        return agent_style_map.get(agent_name.lower(), STYLE_B_PROFESSIONAL_ANALYSIS)

    def get_platform_template(self, platform: str) -> str:
        """根据平台获取写作模板"""
        platform_templates = {
            "zhihu": PLATFORM_ZHIHU_TEMPLATE,
            "wechat": PLATFORM_WECHAT_TEMPLATE,
        }
        return platform_templates.get(platform.lower(), "")

    def get_full_style_guide(self) -> str:
        """获取完整的写作风格指南"""
        return f"""
{WRITING_STYLE_CORE_PRINCIPLES}

{STYLE_A_PERSONAL_NARRATIVE}

{STYLE_B_PROFESSIONAL_ANALYSIS}

{PERSONALITY_EXPRESSIONS}

{AGENT_APPLICATION_GUIDE}
"""

    def format_response_with_style(
        self,
        agent_name: str,
        content: str,
        style_preference: str = "professional"
    ) -> str:
        """根据智能体和风格偏好格式化响应

        Args:
            agent_name: 智能体名称
            content: 原始内容
            style_preference: 风格偏好 (personal/professional/mixed)

        Returns:
            格式化后的内容
        """
        self.get_style_guide(agent_name)

        # 根据不同智能体应用不同的风格特征
        if agent_name.lower() in ["xiaonuo", "小诺"]:
            # 小诺：温暖亲切，家庭关怀
            if "爸爸" not in content and len(content) > 100:
                content = f"爸爸，{content}"
        elif agent_name.lower() in ["xiaona", "小娜"]:
            # 小娜：专业权威，大姐姐关怀
            if not content.startswith("##") and not content.startswith("#"):
                content = f"## 专业分析\n\n{content}"

        return content


